from datetime import date

########################################################################
#                              Operators                               #
########################################################################


def _in(a, b):
    return a in b

########################################################################
#                              Functions                               #
########################################################################


def _date(d):
    if d == "today":
        return date.today()
    else:
        d.strip("'")
        y = int(d[0:4])
        m = int(d[4:6])
        d = int(d[6:8])
    return date(y, m, d)
